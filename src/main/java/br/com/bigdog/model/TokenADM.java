package br.com.bigdog.model;

public class TokenADM {
	// Atributo
	private String token;

	// Getters e Setters
	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	@Override
	public String toString() {
		return "TokenADM [token=" + token + "]";
	}
}
